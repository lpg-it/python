# rang: 顾头不顾尾
r = range(1, 10)
print(r)

for i in range(1, 10):
    print(i)


# 索引
print(r[1])  # 2

for i in range(1, 100, 2):
    print(i)

for i in range(100, 0, -2):
    print(i)


l1 = [1, 2, 3, 'alex', '太白', 2, 3, 4, 66,]
# 利用for循环，利用range将l1列表的所有索引依次打印出来
for i in range(len(l1)):
    print(l1[i])


for i in range(1, 100, -2):
    print(i)


